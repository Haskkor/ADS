
def affichliste(liste):
    for ligne in liste:
        line = ""
        for elem in ligne:
            if elem == 0:
                line += " "
            elif elem == 1:
                line += "#"
            else:
                line += "x"
        print(line)

def affichmessage(message):
    for mot in message:
        word = ""
        for lettre in mot:
            temp = ord(lettre)
            lettre = chr(temp - 1)
            word += lettre
        print(word, end = " ")

